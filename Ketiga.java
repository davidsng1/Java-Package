        System.out.println("nilai min = " + Integer.MIN_VALUE);
        System.out.println("bytes nya integer = " + Integer.BYTES + " bytes");
        System.out.println("bit nya integer = " + Integer.SIZE + " bit");

        //mau bikin byte
        byte b = 5;
        System.out.println("=======BYTE=======");
        System.out.println("nilai byte " + (b + 2));
        System.out.println("nilai max = " + Byte.MAX_VALUE);
        System.out.println("nilai min = " + Byte.MIN_VALUE);
        System.out.println("bytes nya byte = " + Byte.BYTES + " bytes");
        System.out.println("bit nya byte = " + Byte.SIZE + " bit");

        //mau bikin long
        long c = 5L;
        System.out.println("=======LONG=======");
        System.out.println("nilai long " + (c + 2));
        System.out.println("nilai max = " + Long.MAX_VALUE);
        System.out.println("nilai min = " + Long.MIN_VALUE);
        System.out.println("bytes nya long = " + Long.BYTES + " bytes");
        System.out.println("bit nya long = " + Long.SIZE + " bit");

        //mau bikin short
        short d = 5;
        System.out.println("=======SHORT=======");
        System.out.println("nilai short " + (d + 2));
        System.out.println("nilai max = " + Short.MAX_VALUE);
        System.out.println("nilai min = " + Short.MIN_VALUE);
        System.out.println("bytes nya short = " + Short.BYTES + " bytes");
        System.out.println("bit nya short = " + Short.SIZE + " bit");

        //mau bikin float
        float e = 8.99f;
        System.out.println("=======FLOAT=======");
        System.out.println("nilai float " + (e + 2));
        System.out.println("nilai max = " + Float.MAX_VALUE);
        System.out.println("nilai min = " + Float.MIN_VALUE);
        System.out.println("bytes nya float = " + Float.BYTES + " bytes");
        System.out.println("bit nya float = " + Float.SIZE + " bit");
 
        //mau bikin double
        double f = 5.123456789d;
        System.out.println("=======DOUBLE=======");
        System.out.println("nilai double " + (f + 2));

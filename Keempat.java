public class Keempat{

public static void main(String[] args){

    //operasi aritmatika

    int variable1 = 876;
    int variable2 = 666;

    int hasil;
     
    //penjumlahan cok

    hasil = variable1 + variable2;
    System.out.println(variable1 + " + " + variable2 + " = " + hasil);

    //pengurangan cok

    hasil = variable1 - variable2;
    System.out.println(variable1 + " - " + variable2 + " = " + hasil);

    //perkalian cok

    hasil = variable1 * variable2;
    System.out.println(variable1 + " x " + variable2 + " = " + hasil);

    //pembagian cok

    hasil = variable1 / variable2;
    System.out.println(variable1 + " / " + variable2 + " = " + hasil);

    float d = 300;
    float e = 87;
    float hasilFloat = d/e;
    System.out.println("d " + " / " + " e " + " = " + hasilFloat);

    double a = 875.654;
    double b = 762.087;
    double hasilDouble = a/b;
    System.out.println("a " + " / " + " b " + " = " + hasilDouble);

    //modulus cok

    hasil = variable1 % variable2;
    System.out.println(hasil);

    //hasiil pengurangan cok

    hasil = variable1 % variable2;
    System.out.println(hasil);
    
}
}
public class ketujuh {

    public static void main(String[] args){

        // operator assignment penjumlahan
        int a = 120;
        a += 200;
        System.out.println("Nilai a adalah " + a);

        // operator assignment pengurangan
        int b = 120;
        b -= 200;
        System.out.println("Nilai b adalah " + b);

        // operator assignment perkalian
        int c  = 120;
        c *= 200;
        System.out.println("Nilai c adalah " + c);

        // operator assignment pembagian
        int d = 100;
        d /= 2;
        System.out.println("Nilai d adalah " + d);

        // operator assignment modulus
        int e = 20;
        e += 18;
        System.out.println("Nilai e adalah " + e);


     }
    
}
